"""
Validation of CycloneNet's predicted energy source location against
Tropical Cyclone Heat Potential (TCHP) data.
All comments in English.
"""

import json
import logging
from pathlib import Path
import numpy as np
import pandas as pd
import xarray as xr
from scipy.ndimage import maximum_filter, gaussian_filter
from geopy.distance import geodesic

from src.evaluation.evaluate import soft_argmax_2d  # reuse from evaluate.py

logger = logging.getLogger(__name__)


def load_tchp(tchp_path: Path, timestamp, lat, lon, window_deg=5):
    """
    Load TCHP data for a given date and approximate region.
    Expects annual NetCDF files named tchp_YYYY.nc with variable 'tchp'.
    """
    year = timestamp.year
    file = tchp_path / f"tchp_{year}.nc"
    if not file.exists():
        logger.warning(f"TCHP file not found: {file}")
        return None
    ds = xr.open_dataset(file)
    # Select nearest time
    ds_t = ds.sel(time=timestamp, method='nearest')
    # Select region around cyclone
    lon_min, lon_max = lon - window_deg, lon + window_deg
    lat_min, lat_max = lat - window_deg, lat + window_deg
    # Handle longitude wrap (0-360 vs -180-180)
    if ds_t.lon.min() >= 0 and lon_min < 0:
        lon_min += 360
        lon_max += 360
    ds_region = ds_t.sel(lat=slice(lat_min, lat_max),
                         lon=slice(lon_min, lon_max))
    tchp = ds_region['tchp'].values
    lats = ds_region['lat'].values
    lons = ds_region['lon'].values
    ds.close()
    return tchp, lats, lons


def find_tchp_max(tchp: np.ndarray, lats: np.ndarray, lons: np.ndarray, window_px=3):
    """
    Find coordinates of the local maximum of TCHP (smoothed).
    Returns (lat, lon) of the peak.
    """
    # Smooth to reduce noise
    tchp_smooth = gaussian_filter(tchp, sigma=1)
    # Local maxima
    local_max = maximum_filter(tchp_smooth, size=window_px) == tchp_smooth
    peaks = np.argwhere(local_max)
    if len(peaks) == 0:
        # fallback: global maximum
        i, j = np.unravel_index(np.argmax(tchp_smooth), tchp_smooth.shape)
    else:
        # pick peak with highest value
        vals = tchp_smooth[peaks[:, 0], peaks[:, 1]]
        best = peaks[np.argmax(vals)]
        i, j = best
    return float(lats[i]), float(lons[j])


def validate_with_tchp(cfg, predictions_csv: Path, tchp_dir: Path, out_json: Path):
    """
    Read model predictions CSV (generated by evaluate.py) and compare each event
    with the TCHP maximum on the same date and region.
    """
    df = pd.read_csv(predictions_csv)
    results = []
    for _, row in df.iterrows():
        eid = row['event_id']
        y_true = row['y_true']
        y_score = row['y_score']
        pred_lat = row['pred_lat']
        pred_lon = row['pred_lon']

        # Load metadata to get timestamp and true center
        meta_path = Path(cfg['paths']['interim_data']) / f"{eid}.json"
        with open(meta_path) as f:
            meta = json.load(f)
        timestamp = pd.to_datetime(meta['timestamp'])
        center_lat = meta['center_lat']
        center_lon = meta['center_lon']

        tchp_data = load_tchp(tchp_dir, timestamp, center_lat, center_lon)
        if tchp_data is None:
            continue
        tchp, lats_tchp, lons_tchp = tchp_data
        tchp_lat, tchp_lon = find_tchp_max(tchp, lats_tchp, lons_tchp)

        # Calculate distance between prediction and TCHP maximum
        dist_km = geodesic((pred_lat, pred_lon), (tchp_lat, tchp_lon)).km

        results.append({
            'event_id': eid,
            'timestamp': timestamp,
            'center_lat': center_lat,
            'center_lon': center_lon,
            'pred_lat': pred_lat,
            'pred_lon': pred_lon,
            'tchp_max_lat': tchp_lat,
            'tchp_max_lon': tchp_lon,
            'distance_km': dist_km,
            'y_true': y_true,
            'y_score': y_score
        })

    df_out = pd.DataFrame(results)
    out_csv = out_json.with_suffix('.csv')
    df_out.to_csv(out_csv, index=False)

    summary = {
        'mean_distance_km': float(df_out['distance_km'].mean()),
        'median_distance_km': float(df_out['distance_km'].median()),
        'std_distance_km': float(df_out['distance_km'].std()),
        'n_events': len(df_out)
    }
    with open(out_json, 'w') as f:
        json.dump(summary, f, indent=2)

    logger.info(
        f"TCHP validation complete. Mean distance: {summary['mean_distance_km']:.2f} km")
    return summary
